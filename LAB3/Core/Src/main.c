void Start()
{
	 if(start)
	 {
		 time = rand() % (8000 - 3000 + 1) + 3000;
		 start = 0;
		 HAL_Delay(time);
		 led = 1;
		 stimer = HAL_GetTick();
	 }
}


void Play()
{
	if(led == 1)
	{
		if(play){
			rawresult = HAL_GetTick() - stimer ;
			if(rawresult > 1000)
			{
				result[0] = 100;
			}
			else
			{
				result[0] = rawresult / 10;
			}
			eepromExampleWriteFlag = 1;
			EEPROMWriteExample();
			play = 0;
			stimer = 0;
			led = 0;
		}
	}
}

void Recieve()
{
	HAL_UART_Receive(&hlpuart1, RxBuffer, 6, 10000);
	if(RxBuffer[0] == 'a')
	{
		start = RxBuffer[1];
	}
	if(RxBuffer[3] == 'b')
	{
		play = RxBuffer[4];
	}

}

